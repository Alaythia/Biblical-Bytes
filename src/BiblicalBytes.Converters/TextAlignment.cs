namespace BiblicalBytes.Converters;

/// <summary>
/// Defines the text alignment options.
/// </summary>
public enum TextAlignment
{
    /// <summary>
    /// Aligns text to the left.
    /// </summary>
    Left = 0,

    /// <summary>
    /// Aligns text to the right.
    /// </summary>
    Right = 1,

    /// <summary>
    /// Centers text.
    /// </summary>
    Centered = 2,

    /// <summary>
    /// Justifies text, aligning it both to the left and right.
    /// </summary>
    Justified = 3
}